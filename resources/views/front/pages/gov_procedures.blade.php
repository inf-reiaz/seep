									<h5>Objectives</h5>
									As an employer, SEEP , recognizes that from time to time a staff may wish to seek redress for grievances relating to his/her employment.
								</p>
								
								<p>To this end the grievance procedures should be followed where a staff member has a grievance arising from their employment, except where the matter constitutes an appeal against a Termination/Dismissal decision, which cannot be appealed as per SEEP HR Policy.</p>

